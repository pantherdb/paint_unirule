package edu.usc.ksom.pm.panther.paintCommon;

import com.sri.panther.paintCommon.util.StringUtils;
import java.io.Serializable;


public class LabelValue implements Serializable{
    private String label;
    private String value;

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public boolean sameAs(LabelValue comp) {
        String compLabel = comp.getLabel();
        String compValue = comp.getValue();
        return StringUtils.stringsSame(label, compLabel) && StringUtils.stringsSame(value, compValue);

    }
}
